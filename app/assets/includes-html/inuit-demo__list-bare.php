    <h3>The bare list</h3>
    <p>This object is just a way to undress any list of its default styles, mainly remove the dots (or squares or circles) and numbers (on ordered lists) from the list-items and erase the left margin.</p>
    <p>Simply place the <strong>.o-list-bare</strong> class in the <code>ul</code> or <code>ol</code> tag where you want to undress the list.</p>
    <p><strong>Examples:</strong></p>
<code class="block u-margin-bottom">&lt;ul class="<strong>o-list-bare</strong>">
    &lt;li>Bananas...&lt;/li>
    &lt;li>Apples...&lt;/li>
    &lt;li>Oranges...&lt;/li>
&lt;/ul>

&lt;ol class="<strong>o-list-bare</strong>">
    &lt;li>First element...&lt;/li>
    &lt;li>Second element...&lt;/li>
&lt;/ol>
</code>
    <div class="o-layout bg--1 u-margin-bottom">
        <div class="o-layout__item">
            <ul class="o-list-bare">
                <li>Bananas...</li>
                <li>Apples...</li>
                <li>Oranges...</li>
            </ul>
            <ol class="o-list-bare">
                <li>First element...</li>
                <li>Second element...</li>
            </ol>
        </div>
    </div>
