    <h2>Margins</h2>
    <p>There are a few handy classes to manage margins around the blocks.</p>
    <h3 class="u-margin-top-large">Specific widths of margins</h3>
    <p>To apply margins to a block element, simply use one or several classes among these:</p>
    <ul>
        <li><strong>.u-margin</strong>: this class will apply a standard margin on the four sides of the block.</li>
        <li><strong>.u-margin-tiny</strong>, <strong>.u-margin-small</strong>, <strong>.u-margin-large</strong>, <strong>.u-margin-huge</strong>: these classes will apply a <em>tiny</em>, or <em>small</em>, or <em>large</em> or <em>huge</em> margin on the four sides of the block (standard margin sits between <em>small</em> and <em>large</em>).</li>
        <li><strong>.u-margin-none</strong>: this class will remove all margins on the four sides of the block.</li>
    </ul>
    <p><strong>Examples:</strong></p>
<code class="block u-margin-bottom">&lt;div class="<strong>u-margin-tiny</strong>">
    Tiny margin
&lt;/div>

&lt;div class="<strong>u-margin-small</strong>">
    Small margin
&lt;/div>

&lt;div class="<strong>u-margin</strong>">
    Standard margin
&lt;/div>

&lt;div class="<strong>u-margin-large</strong>">
    Large margin
&lt;/div>

&lt;div class="<strong>u-margin-huge</strong>">
    Huge margin
&lt;/div>

&lt;div class="<strong>u-margin-none</strong>">
    No margin
&lt;/div>
</code>
