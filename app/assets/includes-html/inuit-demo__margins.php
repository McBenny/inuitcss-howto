    <h3 class="u-margin-top-large">Selected margins</h3>
    <p>You can also apply the margins selectively: only on the left side, only at the bottom... To achieve this, use this collection of classes: <strong>.u-margin-top</strong>, <strong>.u-margin-right</strong>, <strong>.u-margin-bottom</strong>, <strong>.u-margin-left</strong>, they will respectively apply a standard margin at the top, the right side, at the bottom or on the left side.</p>
    <p><strong>Examples:</strong></p>
<code class="block u-margin-bottom">&lt;div class="<strong>u-margin-left</strong>">
    Left margin
&lt;/div>

&lt;div class="<strong>u-margin-top</strong>">
    Top margin
&lt;/div>

&lt;div class="<strong>u-margin-left u-margin-bottom</strong>">
    Left and bottom margins
&lt;/div>
</code>
