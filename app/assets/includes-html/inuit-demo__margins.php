    <h3 class="u-margin-top-large">Specific widths for selected margins</h3>
    <p>On the same principle, you can add selective margins of specific widths: a tiny left margin, a huge bottom margin, or any combination of these. You can also remove a specific margin on a specific side using the following classes:</p>
    <ul>
        <li><strong>.u-margin-top-tiny</strong>, <strong>.u-margin-top-small</strong>, <strong>.u-margin-top-large</strong>, <strong>.u-margin-top-huge</strong>: to apply different margin widths <em>at the top</em> of the block,</li>
        <li><strong>.u-margin-right-tiny</strong>, <strong>.u-margin-right-small</strong>, <strong>.u-margin-right-large</strong>, <strong>.u-margin-right-huge</strong>: to apply different margin widths <em>on the right side</em> of the block,</li>
        <li><strong>.u-margin-bottom-tiny</strong>, <strong>.u-margin-bottom-small</strong>, <strong>.u-margin-bottom-large</strong>, <strong>.u-margin-bottom-huge</strong>: to apply different margin widths <em>at the bottom</em> of the block,</li>
        <li><strong>.u-margin-left-tiny</strong>, <strong>.u-margin-left-small</strong>, <strong>.u-margin-left-large</strong>, <strong>.u-margin-left-huge</strong>: to apply different margin widths <em>on the left side</em> of the block.</li>
        <li><strong>.u-margin-top-none</strong>, <strong>.u-margin-right-none</strong>, <strong>.u-margin-bottom-none</strong>, <strong>.u-margin-left-none</strong>: to <em>remove margins</em> selectively on a specific side of the block.</li>
    </ul>
    <p><strong>Examples:</strong></p>
<code class="block u-margin-bottom">&lt;div class="<strong>u-margin-left-huge</strong>">
    Huge left margin
&lt;/div>

&lt;div class="<strong>u-margin-top-small u-margin-bottom-large</strong>">
    A small top margin and a large bottom margin
&lt;/div>

&lt;div class="<strong>u-margin u-margin-top-none u-margin-right-huge</strong>">
    Standard margins all around but none at the top and a huge margin on the right side
&lt;/div>
</code>
